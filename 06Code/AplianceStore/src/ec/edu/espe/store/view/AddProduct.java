    public void sizeImage(){
        ImageIcon imagen1= new ImageIcon(getClass().getResource("/images/Consejos-para-cuidar-los-electrodmesticos.jpg"));
        Icon fondo1 = new ImageIcon(imagen1.getImage().getScaledInstance(lbProducts.getWidth(), lbProducts.getHeight(), Image.SCALE_DEFAULT));
        lbProducts.setIcon(fondo1);
    }
    public void addProduct(){
        String serialNumber;
        String price;
        String model;
        String size;
        String brand;
        String type;
        
        serialNumber=txtSerialNumber.getText();
        price = txtPrice.getText();
        model = txtModel.getText();
        size = txtSize.getText();
        brand = txtBrand.getText();
        type = (String) cmbType.getSelectedItem();
        
        
        
        Document dc = new Document();
        ConnectionDataBase conection = new ConnectionDataBase();
        dc.append("type",type).append("serialNumber", serialNumber).append("price",price).append("model",model).append("size", size).append("brand", brand);
        conection.getCollection().insertOne(dc);
        JOptionPane.showMessageDialog(rootPane, "Se han agregado los datos con exito revise la base de datos");
        }
    
