    private void txtSerialNumberFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtSerialNumberFocusLost
      String serialNumber = txtSerialNumber.getText();
          boolean isCharacter;
          isCharacter = InputValidation.validateNumbers(serialNumber);
          if(!(isCharacter)){
            JOptionPane.showMessageDialog(rootPane, "Solo inserte numeros");
            txtSerialNumber.requestFocus();
        }    
    }//GEN-LAST:event_txtSerialNumberFocusLost

    private void txtModelFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtModelFocusLost
          String model = txtModel.getText();
          boolean isCharacter;
          isCharacter = InputValidation.validateCharacters(model);
          if(!(isCharacter)){
            JOptionPane.showMessageDialog(rootPane, "Solo inserte caracteres y sin espacios");
            txtSerialNumber.requestFocus();
        }
    }//GEN-LAST:event_txtModelFocusLost

