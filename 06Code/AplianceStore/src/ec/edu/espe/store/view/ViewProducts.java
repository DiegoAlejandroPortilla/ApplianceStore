    public List<Product> readProduct() {
        String[] columnNames = {"Tipo","N.Serie","Precio","Modelo","Tama√±o","Marca"};
        DefaultTableModel model = new DefaultTableModel(columnNames,0);
        DBCursor cursor = (DBCursor) conection.getCollection().find();
        while (cursor.hasNext()) {
            Product product = new Product((BasicDBObject) cursor.next());
            products.add(product);
            model.addRow(new Object[] { products });
        }
        tbDataView.setModel(model);
        return products;
        
       }
