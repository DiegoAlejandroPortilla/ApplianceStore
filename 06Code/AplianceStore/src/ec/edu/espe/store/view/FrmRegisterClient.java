    private void saveClient() throws HeadlessException, NumberFormatException {
        String name;
        String lastName;
        int id;
        int age;
        String phoneNumber;
        String address;
        String gender;
        Client client;
        String email;

        name = txtName.getText();
        lastName = txtLastName.getText();
        phoneNumber = txtPhoneNumber.getText();
        address = txtAddress.getText();
        gender = getGender();
        email = txtEmail.getText();

        if (emptyValidation(gender)) {
            if (fieldsValidation(gender)) {
                id = Integer.parseInt(txtId.getText());
                age = Integer.parseInt(txtAge.getText());
                client = new Client(name, lastName, id, gender, age, phoneNumber, address, email);
                saveClientsDatabase(FileManager.serializationGson(client));
                JOptionPane.showMessageDialog(this, "El registro de ha realizado correctamente");
                setEmptyFields();
            }
        }

    }

    private boolean fieldsValidation(String gender) {

        setWarnings();
        boolean validation = true;
        JTextField txtGender = new JTextField(gender);

        ArrayList<JTextField> txtsCharacters = new ArrayList<>();
        ArrayList<JTextField> txtsNumbers = new ArrayList<>();

        txtsCharacters.add(txtName);
        txtsCharacters.add(txtLastName);
        txtsNumbers.add(txtId);
        txtsNumbers.add(txtAge);
        txtsNumbers.add(txtPhoneNumber);
        txtsCharacters.add(txtGender);

        for (int i = 0; i < txtsCharacters.size(); i++) {
            if (!InputValidation.validateCharacters(txtsCharacters.get(i).getText())) {
                getWarnings(txtsCharacters.get(i), txtGender);
                validation = false;
            }
        }

        for (int j = 0; j < txtsNumbers.size(); j++) {
            if (!InputValidation.isNumber(txtsNumbers.get(j).getText())) {
                getWarnings(txtsNumbers.get(j), txtGender);
                validation = false;
            }
        }

        if (validation == false) {
            JOptionPane.showMessageDialog(this, "Por favor, Llenar correctamente los campos ", "Error Fields", JOptionPane.WARNING_MESSAGE);
        }
        return validation;
    }

