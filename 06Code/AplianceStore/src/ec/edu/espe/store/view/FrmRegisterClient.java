    private String getGender() {

        String gender = "";

        if (chkMen.isSelected() && !chkOthers.isSelected() && !chkWoman.isSelected()) {
            gender = chkMen.getText();
        } else if (!chkMen.isSelected() && chkOthers.isSelected() && !chkWoman.isSelected()) {
            gender = chkOthers.getText();
        } else if (!chkMen.isSelected() && !chkOthers.isSelected() && chkWoman.isSelected()) {
            gender = chkWoman.getText();
        }

        return gender;
    }

    private void saveClientsDatabase(String jsonObject) {

        Document dc;
        DatabaseConnection connection;

        connection = new DatabaseConnection("Clients");
        dc = Document.parse(jsonObject);
        connection.getCollection().insertOne(dc);
    }

    private boolean emptyValidation(String gender) {

        setWarnings();
        JTextField txtGender = new JTextField(gender);
        boolean validation = true;
        ArrayList<JTextField> txts = new ArrayList<>();

        txts.add(txtName);
        txts.add(txtLastName);
        txts.add(txtId);
        txts.add(txtAge);
        txts.add(txtPhoneNumber);
        txts.add(txtAddress);
        txts.add(txtGender);
        txts.add(txtEmail);

        for (int i = 0; i < txts.size(); i++) {
            if (txts.get(i).getText().trim().isEmpty()) {
                getWarnings(txts.get(i), txtGender);
                validation = false;
            }
        }

        if (validation == false) {

            JOptionPane.showMessageDialog(this, "Por favor, Llene todos los campos ", "Error Empty Fields", JOptionPane.WARNING_MESSAGE);
        }

        return validation;
    }

    private void getWarnings(JTextField txtField, JTextField txtGender) {

        if (txtField.equals(txtName)) {
            lblWarningName.setVisible(true);
        } else if (txtField.equals(txtLastName)) {
            lblWarningLastName.setVisible(true);
        } else if (txtField.equals(txtId)) {
            lblWarningId.setVisible(true);
        } else if (txtField.equals(txtAge)) {
            lblWarningAge.setVisible(true);
        } else if (txtField.equals(txtPhoneNumber)) {
            lblWarningPhoneNumber.setVisible(true);
        } else if (txtField.equals(txtAddress)) {
            lblWarningAddress.setVisible(true);
        } else if (txtField.equals(txtEmail)) {
            lblWarningGender.setVisible(true);
        } else if (txtGender.getText().trim().isEmpty()) {
            lblWarningEmail.setVisible(true);
        }

    }

